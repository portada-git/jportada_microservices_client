    private static class ComboBoxItem{
        private String id;
        private String desc;

        public ComboBoxItem(String string) {
            String[] avalue = string.split(":");
            id=avalue[0];
            if(avalue.length==1){
                desc=avalue[0];
            }else{
                desc=avalue[1];
            }
        }
        
        public ComboBoxItem(String id, String desc) {
            this.id = id;
            this.desc = desc;
        }

        @Override
        public String toString() {
            return desc;
        }

        public String getId() {
            return id;
        }
    }
